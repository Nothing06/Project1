package db;

public class RegContent {
	String regID;
	String  regPassword;
	String regNo;
	String regName;
	String regAge;
	String regPhone;
	public void setRegID(String s)
	{
		regID = s;
	}
	public void setRegPassword(String s)
	{
		regPassword = s;
	}
	public void setRegNo(String s)
	{
		regNo = s;
	}
	public void setRegName(String s)
	{
		regName = s;
	}
	public void setRegAge(String s)
	{
		regAge = s;
	}
	public void setRegPhone(String s)
	{
		regPhone = s;
	}
	public String getRegID()
	{
		return regID;
	}
	public String getRegPassword()
	{
		return regPassword;
	}
	public String getRegNo()
	{
		return regNo;
	}
	public String getRegName()
	{
		return regName;
	}
	public String getRegAge()
	{
		return regAge;
	}
	public String getRegPhone()
	{
		return regPhone;
	}
	
}